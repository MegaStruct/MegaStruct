import UIKit

extension UIColor {
    static var megaRed: UIColor {
        return .init(red: 245/225, green: 78/255, blue: 78/255, alpha: 1)
    }
    
    static var whiteGray: UIColor {
        return .init(red: 246/225, green: 246/255, blue: 246/255, alpha: 1)
    }
}
